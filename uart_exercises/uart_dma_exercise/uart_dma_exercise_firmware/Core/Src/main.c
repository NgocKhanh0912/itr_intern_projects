static void app_start()
{
  uint32_t ret = BSP_UART_OK;

  ret = bsp_uart_start_receive_to_idle_dma(&huart2, dma_buffer, DMA_BUFFER_SIZE);
  if (ret != BSP_UART_OK)
  {
    Error_Handler();
  }

  ret = bsp_uart_register_cb_function(app_rx_data_callback);
  if (ret != BSP_UART_OK)
  {
    Error_Handler();
  }

  handler_buffer = swap_buffer_a;
  reception_buffer = swap_buffer_b;
}

static void app_rx_data_callback(UART_HandleTypeDef *uart, uint16_t size)
{
  if (uart->Instance == huart2.Instance)
  {
    static uint32_t reader = 0;

    if (size != reader)
    {
      if (size > reader)
      {
        received_chars = size - reader;
        for (uint32_t i = 0; i < received_chars; i++)
        {
          reception_buffer[i] = dma_buffer[reader + i];
        }
      }
      else
      {
        received_chars = DMA_BUFFER_SIZE - reader;

        for (uint32_t i = 0; i < received_chars; i++)
        {
          reception_buffer[i] = dma_buffer[reader + i];
        }

        if (size > 0)
        {
          for (uint32_t i = 0; i < size; i++)
          {
            reception_buffer[received_chars + i] = dma_buffer[i];
          }
          received_chars += size;
        }
      }
      // Handle
      app_handler();

      uint8_t *temp = reception_buffer;
      reception_buffer = handler_buffer;
      handler_buffer = temp;
    }
    reader = size;
  }
}

static void app_handler()
{
  uint8_t *temp = reception_buffer;

  for (uint32_t i = 0; i < received_chars; i++)
  {
    // Wait Transmission Enable TXE flag
    while (!(__HAL_UART_GET_FLAG(&huart2, UART_FLAG_TXE)))
      ;
    (&huart2)->Instance->DR = *temp;
    temp++;
  }
}

